function AreaExpanded=MatrixExpand(A)

[m,n,l]=size(A);
B=zeros(m+2,n+2,l+2);
B(2:m+1,2:n+1,2:l+1)=A;
B(1,:,:)=B(2,:,:);% 6 platforms
B(m+2,:,:)=B(m+1,:,:);
B(:,1,:)=B(:,2,:);
B(:,n+2,:)=B(:,n+1,:);
B(:,:,1)=B(:,:,2);
B(:,:,l+2)=B(:,:,l+1);
B(1,1,:)=B(1,2,:);% 12 lines  :top 4 lines
B(1,n+2,:)=B(1,n+1,:);
B(1,:,1)=B(1,:,2);
B(1,:,l+2)=B(1,:,l+1);
B(m+2,1,:)=B(m+2,2,:);% bottom 4 lines
B(m+2,n+2,:)=B(m+2,n+1,:);
B(m+2,:,1)=B(m+2,:,2);
B(m+2,:,l+2)=B(m+2,:,l+1);
B(:,1,1)=B(:,1,2);% middle 4 lines
B(:,1,l+2)=B(:,1,l+1);
B(:,n+2,1)=B(:,n+2,2);
B(:,n+2,l+2)=B(:,n+2,l+1);
B(1,1,1)=B(2,2,2);% 8 voxels
B(1,1,l+2)=B(1,1,l+1);
B(1,n+2,1)=B(1,n+2,2);
B(1,n+2,l+2)=B(1,n+2,l+1);
B(m+2,1,1)=B(m+2,1,2);
B(m+2,1,l+2)=B(m+2,1,l+1);
B(m+2,n+2,1)=B(m+2,n+2,2);
B(m+2,n+2,l+2)=B(m+2,n+2,l+1);
AreaExpanded=B;